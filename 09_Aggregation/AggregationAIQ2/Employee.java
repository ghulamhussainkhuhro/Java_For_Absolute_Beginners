package AggregationAIQ2;

public class Employee {
	private String Ename;
	private String Eposition;
	
	Employee(String Ename, String Eposition){
		this.Ename = Ename;
		this.Eposition = Eposition;
	}
	public String getEname() {
		return Ename;
	}
	public String getEposition() {
		return Eposition;
	}
 
}
